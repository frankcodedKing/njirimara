<p>Four months after he assumed office, Governor Peter Mbah of Enugu State, yesterday, flagged off construction of 71 urban roads with December 2023 as its completion date.
<br>
He also flagged off construction work on the 40 kilometre road linking Owo, in Nkanu East Council, with Isiuzo, in Isiuzo Council, to Obollo, in Nsukka Council of the state.
<br>
Speaking during the flag off of construction of the two kilometre Premier Layout Road, Mbah stated that the funds for the projects had been secured, adding that the state government would stick to the time frame for the projects.</p> <hr>


